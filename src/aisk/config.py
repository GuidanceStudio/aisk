# Commented-out shortcut examples (name, alias) shown in the generated conf.toml.
_SHORTCUT_EXAMPLES = [("gp", "gptpro"), ("cl", "cls"), ("ge", "gef")]


def _render_conf(
    endpoint: str,
    custom_aliases: dict[str, str] | None = None,
    shortcuts: dict[str, str] | None = None,
    *,
    shortcut_examples: bool = False,
) -> str:
    """Render a conf.toml: default alias groups (+ optional custom group),
    the given endpoint, and the given shortcuts."""
    lines = ["[api]", f'endpoint = "{endpoint}"', "", "[aliases]"]
    for i, (label, items) in enumerate(_ALIAS_GROUPS):
        if i > 0:
            lines.append("")
        lines.append(f"# {label}")
        for alias, model in items:
            lines.append(f'{alias} = "{model}"')
    if custom_aliases:
        lines.append("")
        lines.append("# Custom")
        for alias in sorted(custom_aliases):
            lines.append(f'{alias} = "{custom_aliases[alias]}"')

    lines += [
        "",
        "[shortcuts]",
        '# Shell functions generated by `eval "$(aisk completions bash)"`',
        '# Each key becomes a shell function: ds() { aisk dsf "$@"; }',
    ]
    for name, alias in (DEFAULT_SHORTCUTS if shortcuts is None else shortcuts).items():
        lines.append(f'{name} = "{alias}"')
    if shortcut_examples:
        for name, alias in _SHORTCUT_EXAMPLES:
            lines.append(f'# {name} = "{alias}"')

    return "\n".join(lines) + "\n"


def _render_default_conf(endpoint: str = DEFAULT_ENDPOINT) -> str:
    """Render the default conf.toml from _ALIAS_GROUPS + DEFAULT_SHORTCUTS."""
    return _render_conf(endpoint, shortcuts=DEFAULT_SHORTCUTS, shortcut_examples=True)


